 // BrowserSync Server
gulp.task('browser-sync', function() {
  browserSync.init([
    './src/css/*.css',
    './src/js/**/*.js',
    './**/*.html'
  ],
  {
    notify: false,
    server: {
      baseDir: ['./']
    },
    port: 3000,
    browser: ["google chrome"],
    tunnel: false
  });
});


// Default
gulp.task('default', ['browser-sync']);

